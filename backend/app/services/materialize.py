def to_document_reference(
    contract: SummaryContract,
    patient_id: str,
    author_display: str = "Symphony AI",
    pdf_bytes: Optional[bytes] = None,
    tags: Optional[List[str]] = None
) -> Dict[str, Any]:
    """Convert SummaryContract to FHIR DocumentReference resource."""

    doc_ref_id = str(uuid.uuid4())
    timestamp = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%S.%fZ")

    # Create markdown content
    newline = '\n'
    problems_list = ''.join([f'- {p.display}{newline}' for p in contract.problems]) if contract.problems else f'None documented{newline}'
    medications_list = ''.join([f'- {m.display} ({m.status or "unknown status"}){newline}' for m in contract.medications]) if contract.medications else f'None documented{newline}'
    allergies_list = ''.join([f'- {a.display}{newline}' for a in contract.allergies]) if contract.allergies else f'None documented{newline}'
    vitals_list = ''.join([f'- {v.display}: {v.value}{newline}' for v in contract.vitals]) if contract.vitals else f'None documented{newline}'
    labs_list = ''.join([f'- {l.display}: {l.value}{newline}' for l in contract.labs]) if contract.labs else f'None documented{newline}'
    procedures_list = ''.join([f'- {p.display}{newline}' for p in contract.procedures]) if contract.procedures else f'None documented{newline}'
    encounters_list = ''.join([f'- {e.display} ({e.type}){newline}' for e in contract.encounters]) if contract.encounters else f'None documented{newline}'
    care_gaps_list = ''.join([f'- {gap}{newline}' for gap in contract.careGaps]) if contract.careGaps else f'None identified{newline}'
    quality_notes_list = ''.join([f'- {note}{newline}' for note in contract.dataQualityNotes]) if contract.dataQualityNotes else f'No issues noted{newline}'

    markdown_content = f"""# AI Clinical Summary

**Patient:** {patient_id}
**Generated by:** {author_display}
**Date:** {timestamp}

## Problems ({len(contract.problems)})
{problems_list}
## Medications ({len(contract.medications)})
{medications_list}
## Allergies ({len(contract.allergies)})
{allergies_list}
## Vitals ({len(contract.vitals)})
{vitals_list}
## Key Labs ({len(contract.labs)})
{labs_list}
## Procedures ({len(contract.procedures)})
{procedures_list}
## Encounters ({len(contract.encounters)})
{encounters_list}
## Care Gaps ({len(contract.careGaps)})
{care_gaps_list}
## Data Quality Notes ({len(contract.dataQualityNotes)})
{quality_notes_list}"""

    # Create PDF content (placeholder if no bytes provided)
    if pdf_bytes is None:
        # Create a minimal placeholder PDF-like content
        placeholder_content = f"Symphony AI Clinical Summary - Patient {patient_id} - Generated {timestamp}"
        pdf_bytes = base64.b64encode(placeholder_content.encode('utf-8')).decode('utf-8')
    else:
        pdf_bytes = base64.b64encode(pdf_bytes).decode('utf-8')

    # Prepare category with tags
    category = []
    if tags:
        for tag in tags:
            category.append({
                "coding": [{
                    "system": "http://example.org/ai-tags",
                    "code": tag.lower().replace(" ", "-"),
                    "display": tag
                }]
            })

    # Add default AI category
    category.append({
        "coding": [{
            "system": "http://hl7.org/fhir/document-reference-category",
            "code": "clinical-note",
            "display": "Clinical Note"
        }]
    })

    document_reference = {
        "resourceType": "DocumentReference",
        "id": doc_ref_id,
        "status": "current",
        "type": {
            "coding": [{
                "system": "http://loinc.org",
                "code": "11488-4",
                "display": "Consult note"
            }],
            "text": "AI Clinical Note"
        },
        "category": category,
        "subject": {
            "reference": f"Patient/{patient_id}"
        },
        "date": timestamp,
        "author": [{
            "display": author_display
        }],
        "description": "AI-generated clinical summary with structured data extraction",
        "content": [
            {
                "attachment": {
                    "contentType": "text/markdown",
                    "data": base64.b64encode(markdown_content.encode('utf-8')).decode('utf-8'),
                    "title": f"Clinical Summary - {patient_id}"
                }
            },
            {
                "attachment": {
                    "contentType": "application/pdf",
                    "data": pdf_bytes,
                    "title": f"Clinical Summary PDF - {patient_id}"
                }
            }
        ]
    }

    # Add AI tags extension if provided
    if tags:
        document_reference["extension"] = [{
            "url": "http://example.org/fhir/extension/ai-tags",
            "valueString": ", ".join(tags)
        }]

    return document_reference


def make_provenance(
    target_refs: List[str],
    source_bundle_ref: Optional[str] = None,
    author_display: str = "Symphony AI"
) -> Dict[str, Any]:
    """Create FHIR Provenance resource for generated summary resources."""

    provenance_id = str(uuid.uuid4())
    timestamp = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%S.%fZ")

    provenance = {
        "resourceType": "Provenance",
        "id": provenance_id,
        "target": [{"reference": ref} for ref in target_refs],
        "occurredDateTime": timestamp,
        "recorded": timestamp,
        "activity": {
            "coding": [{
                "system": "http://terminology.hl7.org/CodeSystem/v3-DataOperation",
                "code": "DERIVE",
                "display": "derive"
            }]
        },
        "agent": [{
            "type": {
                "coding": [{
                    "system": "http://terminology.hl7.org/CodeSystem/provenance-participant-type",
                    "code": "performer",
                    "display": "Performer"
                }]
            },
            "who": {
                "display": author_display
            }
        }]
    }

    # Add source entity if bundle reference provided
    if source_bundle_ref:
        provenance["entity"] = [{
            "role": "source",
            "what": {
                "reference": source_bundle_ref
            }
        }]

    return provenance


def create_summary_bundle(
    contract: SummaryContract,
    patient_id: str,
    method: str = "composition",
    author_display: str = "Symphony AI",
    pdf_bytes: Optional[bytes] = None,
    doc_ref_tags: Optional[List[str]] = None,
    source_bundle_ref: Optional[str] = None
) -> Dict[str, Any]:
    """Create a transaction bundle with materialized summary resources."""

    bundle_id = str(uuid.uuid4())
    timestamp = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%S.%fZ")

    entries = []
    target_refs = []

    if method in ["composition", "all"]:
        composition = to_composition(contract, patient_id, author_display)
        entries.append({
            "resource": composition,
            "request": {
                "method": "PUT",
                "url": f"Composition/{composition['id']}"
            }
        })
        target_refs.append(f"Composition/{composition['id']}")

    if method in ["lists", "all"]:
        lists = to_lists(contract, patient_id, author_display)
        for list_resource in lists:
            entries.append({
                "resource": list_resource,
                "request": {
                    "method": "PUT",
                    "url": f"List/{list_resource['id']}"
                }
            })
            target_refs.append(f"List/{list_resource['id']}")

    if method in ["document", "all"]:
        doc_ref = to_document_reference(contract, patient_id, author_display, pdf_bytes, doc_ref_tags)
        entries.append({
            "resource": doc_ref,
            "request": {
                "method": "PUT",
                "url": f"DocumentReference/{doc_ref['id']}"
            }
        })
        target_refs.append(f"DocumentReference/{doc_ref['id']}")

    # Always create provenance
    provenance = make_provenance(target_refs, source_bundle_ref, author_display)
    entries.append({
        "resource": provenance,
        "request": {
            "method": "PUT",
            "url": f"Provenance/{provenance['id']}"
        }
    })

    bundle = {
        "resourceType": "Bundle",
        "id": bundle_id,
        "type": "transaction",
        "timestamp": timestamp,
        "entry": entries
    }

    return bundle